var marks = prompt("Enter The Marks for Know Your Grade");

if(marks>=80)
{
    alert("A+");
}
else if(marks>=70)
{
    alert("A");
}
else if(marks>=60)
{
    alert("A-");
}
else if(marks>=50)
{
    alert("B");
}
else if(marks>=40)
{
    alert("C");
}
else if(marks>=33)
{
    alert("D");
}

else{
    alert("Fails");
}

